CREATE VIEW OptionData AS
SELECT 
	-- OptionDataTradingView
	a.symbol, 
	a."option-type", 
	a.strike, 
	a.expiration_date, 
    a.ask, 
	a.bid, 
	a.delta, 
	a.gamma, 
	a.iv, 
	a.rho, 
    a."theoPrice", 
	a.theta, 
	a.vega, 
	a.option, 
	a.time, 
	a.exchange, 
	a.option_osi,

	-- OptionDataYahoo
    b."contractSymbol",  
	b.currency, 
	b."lastPrice", 
	b.change, 
	b."percentChange", 
	b.option_open_interest, 
	b."contractSize", 
	b."lastTradeDate", 
	b."impliedVolatility", 
	b."inTheMoney", 
	b.option_volume,

	-- OptionPricingMetrics
	c.premium_option_price,
	c.intrinsic_value,
	c.extrinsic_value,
	c.moneyness

FROM 
    OptionDataTradingView AS a
JOIN 
    OptionDataYahoo as b
ON a.option_osi = b.contractSymbol
JOIN OptionPricingMetrics as c
ON a.option_osi = c.option_osi;

